//funções aritmeticas

class calculadora
{
    public static double somar(double a, double b)
    {
        return a + b;
    }

    public static double sub(double a, double b)
    {
        return a - b;
    }

    public static double mult(double a, double b)
    {
        return a * b;
    }

    public static double dividir(double a, double b)
    {
        return a / b;
    }

}